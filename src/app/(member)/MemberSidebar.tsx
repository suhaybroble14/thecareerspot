export default function MemberSidebar({
  isAdmin,
  children,
}: {
  isAdmin: boolean;
  children: React.ReactNode;
}) {
  const pathname = usePathname();
  const [mobileNavOpen, setMobileNavOpen] = useState(false);

  const isActive = (href: string) => {
    if (href === "/dashboard") return pathname === "/dashboard";
    if (href === "/admin") return pathname === "/admin";
    return pathname.startsWith(href);
  };

  const NavContent = ({ onLinkClick }: { onLinkClick?: () => void }) => (
    <>
      {/* Member section */}
      <div className="mb-1">
        <p className="px-4 pb-1 text-[10px] tracking-widest uppercase text-forest/30">
          My Account
        </p>
        {memberLinks.map((link) => (
          <Link
            key={link.href}
            href={link.href}
            onClick={onLinkClick}
            className={`flex items-center gap-3 px-4 py-3 text-sm rounded transition-colors ${
              isActive(link.href)
                ? "bg-forest text-cream"
                : "text-forest/70 hover:bg-forest/5 hover:text-forest"
            }`}
          >
            {link.icon}
            {link.label}
          </Link>
        ))}
      </div>

      {/* Admin section */}
      {isAdmin && (
        <div className="mt-4 pt-4 border-t border-forest/10">
          <p className="px-4 pb-1 text-[10px] tracking-widest uppercase text-forest/30">
            Admin
          </p>
          {adminLinks.map((link) => (
            <Link
              key={link.href}
              href={link.href}
              onClick={onLinkClick}
              className={`flex items-center gap-3 px-4 py-3 text-sm rounded transition-colors ${
                isActive(link.href)
                  ? "bg-forest text-cream"
                  : "text-forest/70 hover:bg-forest/5 hover:text-forest"
              }`}
            >
              {link.icon}
              {link.label}
            </Link>
          ))}
        </div>
      )}
    </>
  );

  return (
    <div className="min-h-screen bg-cream">
      {/* Top bar */}
      <header className="fixed top-0 left-0 right-0 z-50 bg-forest h-16 flex items-center justify-between px-6">
        <Link href="/" className="font-serif text-xl tracking-wide text-cream">
          THE{" "}
          <span className="font-bold">
            SP<span className="text-sage">O</span>T
          </span>
        </Link>

        <div className="flex items-center gap-4">
          <Link
            href="/"
            className="hidden sm:inline text-cream/60 text-sm hover:text-cream transition-colors"
          >
            Back to site
          </Link>
          <form action="/api/auth/signout" method="POST">
            <button
              type="submit"
              className="text-cream/60 text-sm hover:text-cream transition-colors"
            >
              Sign out
            </button>
          </form>

          {/* Mobile menu toggle */}
          <button
            onClick={() => setMobileNavOpen(!mobileNavOpen)}
            className="md:hidden text-cream p-1"
            aria-label="Toggle navigation"
          >
            <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24" strokeWidth={1.5}>
              {mobileNavOpen ? (
                <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
              ) : (
                <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
              )}
            </svg>
          </button>
        </div>
      </header>

      <div className="flex pt-16">
        {/* Desktop sidebar */}
        <aside className="hidden md:flex flex-col w-60 fixed top-16 bottom-0 bg-white border-r border-forest/10 py-6 px-3 overflow-y-auto">
          <nav className="flex flex-col gap-1">
            <NavContent />
          </nav>
        </aside>

        {/* Mobile dropdown nav */}
        <AnimatePresence>
          {mobileNavOpen && (
            <motion.div
              initial={{ opacity: 0, y: -10 }}
              animate={{ opacity: 1, y: 0 }}
              exit={{ opacity: 0, y: -10 }}
              className="fixed top-16 left-0 right-0 z-40 bg-white border-b border-forest/10 md:hidden overflow-y-auto max-h-[80vh]"
            >
              <nav className="flex flex-col p-3">
                <NavContent onLinkClick={() => setMobileNavOpen(false)} />
              </nav>
            </motion.div>
          )}
        </AnimatePresence>

        {/* Main content */}
        <main className="flex-1 md:ml-60 min-h-[calc(100vh-4rem)]">
          <div className="max-w-4xl mx-auto px-6 py-8">
            {children}
          </div>
        </main>
      </div>
    </div>
  );
}
